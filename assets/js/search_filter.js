        image = data[i].image;

    // Create product cards
    products += "<div class='col-sm-4 product' data-make='" + make + "' data-model='" + model + "' data-type='" + type + "'><div class='product-inner text-center'><img src='" + image + "'>" +  type + "</div></div>";

